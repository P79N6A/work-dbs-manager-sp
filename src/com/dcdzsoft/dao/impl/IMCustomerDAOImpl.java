	public IMCustomer find(IMCustomer obj) throws EduException
	{
		IMCustomer result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT CustomerID,TerminalNo,CustomerName,IDCard,Address,Email,Mobile,Active,Status,ActiveCode,Validity,keepMonths,CreateTime,LastModifyTime,Remark  FROM IMCustomer WHERE CustomerID = ? ";

		log.debug("[find sql]:" + "SELECT CustomerID,TerminalNo,CustomerName,IDCard,Address,Email,Mobile,Active,Status,ActiveCode,Validity,keepMonths,CreateTime,LastModifyTime,Remark  FROM IMCustomer WHERE CustomerID = " + StringUtils.addQuote(obj.CustomerID) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.CustomerID);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new IMCustomer();
				result.CustomerID = rset.getString(1);
				result.TerminalNo = rset.getString(2);
				result.CustomerName = rset.getString(3);
				result.IDCard = rset.getString(4);
				result.Address = rset.getString(5);
				result.Email = rset.getString(6);
				result.Mobile = rset.getString(7);
				result.Active = rset.getString(8);
				result.Status = rset.getString(9);
				result.ActiveCode = rset.getString(10);
				result.Validity = rset.getTimestamp(11);
				result.keepMonths = rset.getInt(12);
				result.CreateTime = rset.getTimestamp(13);
				result.LastModifyTime = rset.getTimestamp(14);
				result.Remark = rset.getString(15);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_IMCUSTOMERNODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYIMCUSTOMERFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


