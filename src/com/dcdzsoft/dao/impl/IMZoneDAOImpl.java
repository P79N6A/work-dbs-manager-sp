	public IMZone find(IMZone obj) throws EduException
	{
		IMZone result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT ZoneID,CompanyID,ZoneName,Description,CollectCharge,MandatoryFlag,CreateTime,LastModifyTime,Remark  FROM IMZone WHERE ZoneID = ? ";

		log.debug("[find sql]:" + "SELECT ZoneID,CompanyID,ZoneName,Description,CollectCharge,MandatoryFlag,CreateTime,LastModifyTime,Remark  FROM IMZone WHERE ZoneID = " + StringUtils.addQuote(obj.ZoneID) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.ZoneID);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new IMZone();
				result.ZoneID = rset.getString(1);
				result.CompanyID = rset.getString(2);
				result.ZoneName = rset.getString(3);
				result.Description = rset.getString(4);
				result.CollectCharge = rset.getDouble(5);
				result.MandatoryFlag = rset.getInt(6);
				result.CreateTime = rset.getTimestamp(7);
				result.LastModifyTime = rset.getTimestamp(8);
				result.Remark = rset.getString(9);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_IMZONENODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYIMZONEFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


